function barrejarArray(array) {
    const arrayBarrejat = [...array]; // Copia l'array original per no modificar-lo directament.
  
    for (let i = arrayBarrejat.length - 1; i > 0; i--) {
      // Genera un índex aleatori per a cada element de l'array.
      const j = Math.floor(Math.random() * (i + 1));
  
      // Intercanvia els elements a les posicions i i j.
      [arrayBarrejat[i], arrayBarrejat[j]] = [arrayBarrejat[j], arrayBarrejat[i]];
    }
  
    // Retorna l'array barrejat i actualitza l'estat intern.
    barrejarArray.arrayBarrejat = arrayBarrejat;
  
    return arrayBarrejat;
  }
  
